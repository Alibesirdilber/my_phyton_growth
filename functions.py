def say_hello_to(name):
    # 'name' is the parameter
    print(f"Hello, {name}! How are you today?")

# Calling the function with an argument ('Alice')
say_hello_to("Alice")
# Output: Hello, Alice! How are you today?

# Calling the function with a different argument ('Bob')
say_hello_to("Bob")
# Output: Hello, Bob! How are you today?